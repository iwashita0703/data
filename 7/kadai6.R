# ---------------------------------------------------------
# 14-1A：体重と身長のピアソン相関係数を求め、図の左上に表示（提出用）
# ---------------------------------------------------------

# CSV 読み込み
Body <- read.table("SampleData03_forR2.csv", header=TRUE, sep=",")
pdf("Class07_rp14-1A_02.pdf")
# ---------------------------------------------------------
# ★ 散布図（白抜き四角）
# ---------------------------------------------------------
plot(Body[,7], Body[,8],
     xlab="Weight", ylab="Height",
     pch=22, col="black", bg="white", cex=1.8,
     main="Correlation between Weight and Height (Pearson)")

# ---------------------------------------------------------
# ★ ピアソンの積率相関係数を計算
# ---------------------------------------------------------
Body_cor <- cor.test(Body[,7], Body[,8], method="pearson")
r_value <- Body_cor$estimate   # 相関係数 r

# ---------------------------------------------------------
# ★ 左上に相関係数 r を表示
# ---------------------------------------------------------
text(min(Body[,7]) + 2,
     max(Body[,8]) - 20,
     sprintf("r = %.7f", r_value),
     col="black", cex=0.9, adj=c(0,1))
    dev.off()